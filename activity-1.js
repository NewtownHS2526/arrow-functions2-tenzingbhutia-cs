// Activity 1: Basic Function Declarations to Arrow Functions
// Convert each function below to an arrow function

// Problem 1
function greet() {
    return "Hello, World!";
}
    
// Convert to arrow function:
const greet = () => "Hello, World!";

// Problem 2
function sayGoodbye() {
    console.log("Goodbye!");
}
// Convert to arrow function:
const sayGoodbye = () => "Goodbye!";


// Problem 3
